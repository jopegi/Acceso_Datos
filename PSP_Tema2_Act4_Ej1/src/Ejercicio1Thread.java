
public class Ejercicio1Thread extends Thread {
	////////////////////////////////////////////////////////////////////////
	////////////////// Atributos de la clase/////////////////////////////////
	////////////////////////////////////////////////////////////////////////
	
	private String name; 			//Definirá el nombre del hilo
	private String message; 		//Definirá un mensaje a mostrar por consola
	private int delay = 0; 			//Definirá un retardo
	private int iteraciones = 3;	//Controlador del número de iteraciones en el método run()
	
	////////////////////////////////////////////////////////////////////////
	/////////////////// Fin atributos de la clase////////////////////////////
	////////////////////////////////////////////////////////////////////////

	/**********************************************************************/
	////////////////////////////////////////////////////////////////////////
	///////////////// Constructores de la clase//////////////////////////////
	////////////////////////////////////////////////////////////////////////
	
	public Ejercicio1Thread(String name, String message, int delay) {

		this.name = name;
		this.message = message;
		this.delay = delay;

	}

	public Ejercicio1Thread(String name, String message) {

		this.name = name;
		this.message = message;

	}

	////////////////////////////////////////////////////////////////////////
	///////////////// Fin constructores de la clase//////////////////////////
	////////////////////////////////////////////////////////////////////////
	/**********************************************************************/
	////////////////////////////////////////////////////////////////////////
	///////////////// Método run() de entrada al Thread//////////////////////
	////////////////////////////////////////////////////////////////////////
	
	public void run() {
		
		try {

			sleep(delay);

		} catch (InterruptedException e) {

			e.printStackTrace();
		}

		//Al inicio de un hilo se mostrará el siguiente mensaje
		System.out.println("Inicio hilo: " + name);

		//Iteramos cada hilo n veces
		for (int i = 0; i < iteraciones; i++) {

			System.out.println(message);

		}

		// Cuando terminen las iteraciones se lanzará el siguiente mensaje
		System.out.println("Final hilo:  " + name);
	}
	
	////////////////////////////////////////////////////////////////////////
	///////////////// Fin método run() de entrada al Thread//////////////////
	////////////////////////////////////////////////////////////////////////
	/**********************************************************************/
	////////////////////////////////////////////////////////////////////////
	///////////////// Métodos setter y getter///////////////////////////////
	////////////////////////////////////////////////////////////////////////
	
	public int getIteraciones() {
		return iteraciones;
	}

	public void setIteraciones(int iteraciones) {
		this.iteraciones = iteraciones;
	}
	
	////////////////////////////////////////////////////////////////////////
	///////////////// Fin métodos setter y getter///////////////////////////
	////////////////////////////////////////////////////////////////////////
	/**********************************************************************/
	////////////////////////////////////////////////////////////////////////
	/////////////////Método main() de entrada a la app//////////////////////
	////////////////////////////////////////////////////////////////////////
	
	public static void main(String[] args) {

		Thread hilo1 = new Ejercicio1Thread("hilo1","hilo 1 aquí...");
		Thread hilo2 = new Ejercicio1Thread("hilo2","hilo 2 aquí...");
		
		
		hilo1.start();
		hilo2.start();
		
		//Si queremos asegurarnos de que el println() del main se muestre después de ejecutarse los hilos
		//deberemos descomentar el siguiente bloque de código:
		
		/*try {
			
			//Con el join() nos aseguramos de que hasta que no finalicen los hilos lanzados
			//no continue el hilo de ejecución
			hilo1.join();
			hilo2.join();
			
		} catch (InterruptedException e) {

			e.printStackTrace();
		}*/
		
		for (int j=0;j<3;j++) {
			System.out.println("main aquí...");
		}

	}
	
	////////////////////////////////////////////////////////////////////////
	/////////////////Fin método main() de entrada a la app///////////////////
	////////////////////////////////////////////////////////////////////////

}
